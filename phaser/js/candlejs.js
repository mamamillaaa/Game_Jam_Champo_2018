function create() {
	game.physics.startSystem(Phaser.Physics.ARCADE);

	platforms = game.add.group();
	platforms.enableBody = true;
    game.world.setBounds(0, 0, map.length*64, map.width*64);

    createMap();

	player = game.add.sprite(100, 1750, 'dude');
	game.physics.arcade.enable(player);
	player.body.bounce.y = 0.2;
    player.body.gravity.y = 300;
    player.body.collideWorldBounds = true;
	player.animations.add('left', [0, 1, 2, 3], 10, true);
    player.animations.add('right', [5, 6, 7, 8], 10, true);
    this.jumping = false;
    game.camera.follow(player);

    ennemygroup.enableBody=true;
    ennemygroup.physicsBodyType= Phaser.Physics.ARCADE;

    this.jumping = false;
    game.camera.follow(player);

    cursors = game.input.keyboard.createCursorKeys();
}

function update() {
	game.physics.arcade.collide(player, platforms);
    game.physics.arcade.collide(player, ennemygroup, loseLife, null, this);
	player.body.velocity.x = 0;
    if (cursors.left.isDown)
    {
        player.body.velocity.x = -SPEED;
        player.animations.play('left');
    }
    else if (cursors.right.isDown)
    {
        player.body.velocity.x = SPEED;
        player.animations.play('right');
    }
    else
    {
        player.animations.stop();
        player.frame = 4;
    }

    var onTheGround = player.body.touching.down;
    if (onTheGround) {
        this.jumps = JUMPS;
        this.jumping = false;
    }
    if (this.jumps == 2 && cursors.up.isDown && cursors.up.duration < 200) {
        player.body.velocity.y = -JUMPSPEED;
        this.jumping = true;
    }
    if (this.jumps == 1 && cursors.up.isDown && cursors.up.duration < 200) {
        player.body.velocity.y = -DOUBLEJUMPSPEED;
        this.jumping = true;
    }
    if (this.jumping && cursors.up.isUp) {
        this.jumps--;
        this.jumping = false;
    }
}

function createMap(){
    for (var i=0; i<map.length; i++){
        for (var j=0; j<map[i].length; j++){
            if (map[i][j] == 1){
                var ground = platforms.create(j*64, i*64, 'ground');
                ground.scale.setTo(2, 2);
                ground.body.immovable = true;
            }
        }
    }
}

